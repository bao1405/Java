public class bai2 {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int sum = a + b;
        int difference = a - b;
        int product = a * b;
        int remainder = a % b;
        System.out.println("a = " + a +"b = " + b);
        System.out.println("Tổng của a + b: " + sum);
        System.out.println("Hiệu của a = b: " + difference);
        System.out.println("Tích của a + b: " + product);
        System.out.println("Phần dư khi chia a cho b: " + remainder);
    }
}
